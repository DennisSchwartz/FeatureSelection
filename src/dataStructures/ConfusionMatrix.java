package dataStructures;

/**
 * ConfusionMatrix stores the data of a ConfusionMatrix. 
 * It's a 2x2 Matrix with the fields truePositive, trueNegative, falsePositive, falseNegative. 
 * @author julianschwab
 *
 */

public class ConfusionMatrix {
	
	private int truePositive;
	private int trueNegative;
	private int falsePositive;
	private int falseNegative;

	/**
	 * constructor for a ConfusionMatrix Object
	 * @param truePositive
	 * @param falsePositive
	 * @param trueNegative
	 * @param falseNegative
	 */
	public ConfusionMatrix(int truePositive, int falsePositive, int trueNegative, int falseNegative) {
		this.truePositive = truePositive;
		this.falsePositive = falsePositive;
		this.trueNegative = trueNegative;
		this.falseNegative = falseNegative;
	}

	/**
	 * @return the truePositive
	 */
	public int getTruePositive() {
		return truePositive;
	}

	/**
	 * @return the trueNegative
	 */
	public int getTrueNegative() {
		return trueNegative;
	}

	/**
	 * @return the falsePositive
	 */
	public int getFalsePositive() {
		return falsePositive;
	}

	/**
	 * @return the falseNegative
	 */
	public int getFalseNegative() {
		return falseNegative;
	}
	
	public String toString() {
		return "{ " + truePositive + "\t" + trueNegative + "\n" +
			   "  " + falsePositive + "\t" + falseNegative;
	}
	
	
}
